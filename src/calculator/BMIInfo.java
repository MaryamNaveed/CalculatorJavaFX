package calculator;

public class BMIInfo {
	String weight;
	String height;

	public BMIInfo() {
	}

	public BMIInfo(String weight, String height) {
		this.weight = weight;
		this.height = height;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

	public String getHeight() {
		return height;
	}

	public void setHeight(String height) {
		this.height = height;
	}
	
	

}
